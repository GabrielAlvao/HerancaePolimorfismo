package HerançaeInterface;

public class Animal {
	
	private String nome;
	private int idade;
	
	public void setnome(String nome){
		this.nome = nome;
	}
	public String getnome() {
		return nome;
	}
	public void setidade(int idade){
		this.idade = idade;
	}
	public int getidade() {
		return idade;
	}
	
	
}